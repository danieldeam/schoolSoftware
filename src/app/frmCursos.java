    public void listar() {
        String SQL = "SELECT * FROM cursos";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(SQL);
            Object[] cursos = new Object[2];
            modelo = (DefaultTableModel) tblCursos.getModel();

            while (rs.next()) {
                cursos[0] = rs.getInt("id_curso");
                cursos[1] = rs.getString("curso");
                modelo.addRow(cursos);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void limpiar_tabla(DefaultTableModel modelo) {
        for (int i = 0; i < tblCursos.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

    public void agregar() {
        curso = txtCurso.getText();
        id = Integer.parseInt(txtId.getText());
        if (curso.equals("") || Integer.toString(id).equals("")) {
            JOptionPane.showMessageDialog(null, "Debe llenar los campos");
            limpiar_tabla(modelo);
            txtCurso.requestFocus();
        } else {
            String SQL = "INSERT INTO cursos (cursos,id_curso) VALUES ('" + curso + "','" + id + "')";
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(SQL);
                JOptionPane.showMessageDialog(null, "Registro exitoso");
                limpiar_tabla(modelo);
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }

    public void modificar() {
        curso = txtCurso.getText();
        id = Integer.parseInt(txtId.getText());
        String SQL = "UPDATE cursos SET cursos='" + curso + "', id_curso='" + id + "' WHERE id_curso='" + id + "'";
        if (curso.equals("") || Integer.toString(id).equals("")) {
            JOptionPane.showConfirmDialog(null, "Debe llenar los campos");
        } else {
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(SQL);
                JOptionPane.showMessageDialog(null, "Registro actualizado");
                limpiar_tabla(modelo);
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }

    public void eliminar() {
        String SQL = "DELETE FROM cursos WHERE cursos='" + curso + "'";
        int seleccionado = tblCursos.getSelectedRow();
        if (seleccionado < 0) {
            JOptionPane.showMessageDialog(null, "Registro no seleccionado");
            limpiar_tabla(modelo);
        } else {
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(SQL);
                JOptionPane.showMessageDialog(null, "Registro Eliminado con Ã‰xito");
                limpiar_tabla(modelo);
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }

