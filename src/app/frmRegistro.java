    public void limpiar_campos() {
        txtUsuario.setText("");
        txtpwd.setText("");
        cboRol.setSelectedItem("Seleccione");
    }

    public void agregar() {
        user = txtUsuario.getText();
        pass = String.valueOf(txtpwd.getPassword());

        if (user.equals("") || pass.equals("") || cboRol.getSelectedItem().equals("Seleccione")) {
            JOptionPane.showMessageDialog(null, "Debe digitar los campos");
        } else {
            if (cboRol.getSelectedItem().equals("Profesor")) {
                String SQL = "INSERT INTO usuarios (usuario,pass,rol) VALUES('" + user + "','" + pass + "','Profesor')";
                try {
                    cn = con.getConnection();
                    st = cn.createStatement();
                    st.executeUpdate(SQL);
                    JOptionPane.showMessageDialog(null, "Registro exitoso");
                    limpiar_campos();
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            } else if (cboRol.getSelectedItem().equals("Estudiante")) {
                String SQL = "INSERT INTO usuarios (usuario,pass,rol) VALUES('" + user + "','" + pass + "','Estudiante')";
                try {
                    cn = con.getConnection();
                    st = cn.createStatement();
                    st.executeUpdate(SQL);
                    JOptionPane.showMessageDialog(null, "Registro exitoso");
                    limpiar_campos();
                } catch (HeadlessException | SQLException e) {
                    JOptionPane.showMessageDialog(null, e.getMessage());
                }
            }
        }

    }

