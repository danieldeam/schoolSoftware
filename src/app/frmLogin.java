    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        String usuario = txtUser.getText();
        String pass = new String(txtpwd.getPassword());

        if (usuario.equals("") || pass.equals("") || cboType.getSelectedItem().equals("Seleccione")) {
            JOptionPane.showMessageDialog(null, "Por favor llene todos los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtUser.requestFocus();
        } else if(cboType.getSelectedItem().equals("Rector")){
            App.tipo_usuario = "Rector";
            validar_usuario();
            limpiar_cajas();
        } else if (cboType.getSelectedItem().equals("Profesor")) {
            App.tipo_usuario = "Profesor";
            validar_usuario();
            limpiar_cajas();
        } else if (cboType.getSelectedItem().equals("Estudiante")) {
            App.tipo_usuario ="Estudiante";
            validar_usuario();
            limpiar_cajas();
            
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

