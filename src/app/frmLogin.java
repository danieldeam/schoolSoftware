    public void validar_usuario() {
        
        int resultado = 0;
        String pass = String.valueOf(txtpwd.getPassword());
        String usuario = txtUser.getText();
        String tipo = App.tipo_usuario;
        String SQL = "SELECT * FROM usuarios WHERE usuario='" + usuario + "' and pass='" + pass + "' and rol='" + tipo + "'";

        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(SQL);

            if (rs.next()) {
                resultado = 1;
                if (resultado == 1) {
                    frmPrincipal ppal = new frmPrincipal();
                    ppal.setLocationRelativeTo(null);
                    ppal.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error de Acceso, Usuario No Registrado");
            }

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

